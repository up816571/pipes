    private void pipeLengthFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pipeLengthFocusLost
        double input;
        try {
            input = Double.parseDouble(pipeLength.getText());
            if (input >= 0.5 && input <= 6) {
                length = input;
            } else {
                length = 0;
            }
        } catch (Exception e) {
            //do error
        }
    }//GEN-LAST:event_pipeLengthFocusLost

